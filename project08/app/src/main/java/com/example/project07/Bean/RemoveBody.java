package com.example.project07.Bean;

public class RemoveBody {
    private String address;
    private String area;
    private String city;
    private String idCard;
    private String photo;
    private String plateNo;
    private String province;
    private String tel;

    public RemoveBody(String address, String area, String city, String idCard, String photo, String plateNo, String province, String tel) {
        this.address = address;
        this.area = area;
        this.city = city;
        this.idCard = idCard;
        this.photo = photo;
        this.plateNo = plateNo;
        this.province = province;
        this.tel = tel;
    }
}
